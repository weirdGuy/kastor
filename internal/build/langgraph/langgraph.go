// Package langgraph generates a runnable LangGraph (Python) project from a
// loaded ADL module — the codegen path of SPEC.md §1 (issue #12).
//
// Block → Python mapping:
//
//	model  "m" → models.py: factory m() returning init_chat_model("<provider>:<id>", **params)
//	prompt "p" → prompts/p.py: TEMPLATE constant + shared prompts.render for {{var}}
//	tool   "t" → tools/t.py: a langchain @tool function; the body is the source binding
//	agent  "a" → agents/a.py: pydantic Output model (the IO contract) + run(<inputs>)
//	             that renders the system prompt and invokes create_agent
//
// Tool source.kind mapping:
//
//	mcp     → call through mcp_support.call_mcp_tool; the spec pins
//	          mcp://<server>/<tool>, connection details are deployment
//	          config read from mcp_servers.json at runtime
//	http    → POST the parameters as JSON to the source uri (stdlib urllib)
//	runtime → stub raising NotImplementedError until the user fills it in
//	builtin → codegen error: platform-provided tools have no local binding
//	script  → codegen error: deferred, not in v0 langgraph scope
//
// Cross-agent data flow (input defaults like agent.x.output.y) is not wired
// in v0: run() takes those inputs as explicit optional parameters.
// depends_on never affects generated code (SPEC.md §4: ordering only).
package langgraph

import (
	"fmt"
	"sort"
	"strings"

	"github.com/weirdGuy/agentform/internal/build"
	"github.com/weirdGuy/agentform/internal/schema"
)

// Generator implements build.Generator for the LangGraph target.
type Generator struct{}

var _ build.Generator = Generator{}

// Generate emits the whole Python project. Every block in the module is
// emitted — reachability from an agent is not required — and all block
// slices are re-sorted by name so output never depends on file layout.
func (Generator) Generate(job *build.Job) ([]build.File, error) {
	mod := job.Module
	agents := sortedCopy(mod.Agents, func(a *schema.Agent) string { return a.Name })
	tools := sortedCopy(mod.Tools, func(t *schema.Tool) string { return t.Name })
	prompts := sortedCopy(mod.Prompts, func(p *schema.Prompt) string { return p.Name })
	models := sortedCopy(mod.Models, func(m *schema.Model) string { return m.Name })

	var files []build.File
	add := func(path string, data []byte) {
		files = append(files, build.File{Path: path, Data: data})
	}

	modelsPy, err := genModels(models)
	if err != nil {
		return nil, err
	}
	add("models.py", modelsPy)

	add("prompts/__init__.py", genPromptsInit())
	for _, p := range prompts {
		add("prompts/"+pyIdent(p.Name)+".py", genPrompt(p))
	}

	add("tools/__init__.py", pkgInit("Tool bindings generated from the module's tool blocks."))
	hasMCP, hasRuntime := false, false
	for _, t := range tools {
		data, err := genTool(t)
		if err != nil {
			return nil, err
		}
		add("tools/"+pyIdent(t.Name)+".py", data)
		switch t.Source.Kind {
		case "mcp":
			hasMCP = true
		case "runtime":
			hasRuntime = true
		}
	}
	if hasMCP {
		add("mcp_support.py", genMCPSupport())
	}

	add("agents/__init__.py", pkgInit("Agents generated from the module's agent blocks."))
	for _, a := range agents {
		data, err := genAgent(a)
		if err != nil {
			return nil, err
		}
		add("agents/"+pyIdent(a.Name)+".py", data)
	}

	add("main.py", genMain(agents))
	add("requirements.txt", genRequirements(models, hasMCP))
	add("README.md", genReadme(job.Target, agents, tools, models, hasMCP, hasRuntime))
	return files, nil
}

// pkgInit emits a docstring-only package __init__.py.
func pkgInit(doc string) []byte {
	return []byte("\"\"\"" + doc + "\n\nGenerated by adl build. Do not edit.\n\"\"\"\n")
}

// genMain emits main.py: a small CLI that runs one agent by name with its
// inputs passed as a JSON object.
func genMain(agents []*schema.Agent) []byte {
	var b strings.Builder
	b.WriteString(`"""Run one generated agent from the command line.

Generated by adl build. Do not edit.

Usage: python main.py <agent> --inputs '{"<input>": <value>, ...}'
"""

import argparse
import json
`)
	if len(agents) > 0 {
		names := make([]string, 0, len(agents))
		for _, a := range agents {
			names = append(names, pyIdent(a.Name))
		}
		fmt.Fprintf(&b, "\nfrom agents import %s\n", strings.Join(names, ", "))
	}

	if len(agents) == 0 {
		b.WriteString("\nAGENTS = {}\n")
	} else {
		b.WriteString("\nAGENTS = {\n")
		for _, a := range agents {
			fmt.Fprintf(&b, "    %s: %s.run,\n", pyString(a.Name), pyIdent(a.Name))
		}
		b.WriteString("}\n")
	}

	b.WriteString(`

def main():
    parser = argparse.ArgumentParser(description="Run an agent generated by adl build.")
    parser.add_argument("agent", choices=sorted(AGENTS))
    parser.add_argument(
        "--inputs",
        default="{}",
        help="JSON object holding the agent's declared inputs",
    )
    args = parser.parse_args()

    result = AGENTS[args.agent](**json.loads(args.inputs))
    if hasattr(result, "model_dump_json"):
        print(result.model_dump_json(indent=2))
    else:
        print(result)


if __name__ == "__main__":
    main()
`)
	return []byte(b.String())
}

// genRequirements emits requirements.txt. The project is deliberately not
// packaged (no pyproject): it is an application run in place, and a plain
// requirements file keeps setup to venv + pip install.
func genRequirements(models []*schema.Model, hasMCP bool) []byte {
	deps := map[string]bool{
		"langchain>=1.0": true,
		"langgraph>=1.0": true,
		"pydantic>=2":    true,
	}
	if hasMCP {
		deps["langchain-mcp-adapters"] = true
	}
	for _, m := range models {
		if p, ok := providers[m.Provider]; ok {
			deps[p.Dep] = true
		}
	}

	lines := make([]string, 0, len(deps))
	for dep := range deps {
		lines = append(lines, dep)
	}
	sort.Strings(lines)
	return []byte("# Generated by adl build. Do not edit.\n" + strings.Join(lines, "\n") + "\n")
}

// genReadme emits README.md: setup, credentials, how to run, and the
// module's agents and tool bindings at a glance.
func genReadme(tgt *schema.Target, agents []*schema.Agent, tools []*schema.Tool, models []*schema.Model, hasMCP, hasRuntime bool) []byte {
	var b strings.Builder
	fmt.Fprintf(&b, "# %s — LangGraph project\n\n", tgt.Addr())
	b.WriteString("Generated by `adl build`. Do not edit files in this directory: the next\nbuild rewrites them (hidden entries like `.venv` are left alone).\n")

	b.WriteString("\n## Setup\n\n```sh\npython -m venv .venv\n. .venv/bin/activate\npip install -r requirements.txt\n```\n")
	if len(models) > 0 {
		b.WriteString("\nModel credentials come from the environment:\n\n")
		for _, m := range models {
			p := providers[m.Provider]
			if p.EnvVar == "" {
				fmt.Fprintf(&b, "- `%s` (provider `%s`): no API key, runs locally\n", m.Addr(), m.Provider)
			} else {
				fmt.Fprintf(&b, "- `%s` (provider `%s`): set `%s`\n", m.Addr(), m.Provider, p.EnvVar)
			}
		}
	}

	if len(agents) > 0 {
		b.WriteString("\n## Run\n\n```sh\n")
		fmt.Fprintf(&b, "python main.py %s --inputs '%s'\n", agents[0].Name, exampleInputs(agents[0]))
		b.WriteString("```\n\nAgents:\n\n")
		for _, a := range agents {
			fmt.Fprintf(&b, "- `%s` — inputs: %s; outputs: %s\n", a.Addr(), inputList(a), outputList(a))
		}
	}

	if len(tools) > 0 {
		b.WriteString("\nTools:\n\n")
		for _, t := range tools {
			fmt.Fprintf(&b, "- `%s` (%s) — %s\n", t.Addr(), t.Source.Kind, toolBinding(t))
		}
	}

	if hasMCP {
		b.WriteString("\n## MCP servers\n\n")
		b.WriteString("Tools with `kind = \"mcp\"` read server connection details from\n`mcp_servers.json` in the working directory (or the file named by\n`ADL_MCP_CONFIG`), keyed by server name:\n\n")
		b.WriteString("```json\n{\n  \"<server>\": {\n    \"transport\": \"stdio\",\n    \"command\": \"your-mcp-server\",\n    \"args\": []\n  }\n}\n```\n")
	}

	if hasRuntime {
		b.WriteString("\n## Runtime tools\n\n")
		b.WriteString("Tools with `kind = \"runtime\"` are generated as stubs that raise\n`NotImplementedError`. `adl build` regenerates them on every run, so keep\nthe real implementation in code you own and call it from the stub body.\n")
	}
	return []byte(b.String())
}

// exampleInputs renders a JSON object of an agent's required inputs with
// placeholder values, for the README run example.
func exampleInputs(a *schema.Agent) string {
	var parts []string
	for _, in := range a.Inputs {
		if !in.Optional && in.Default == nil && in.DefaultRef == "" {
			parts = append(parts, fmt.Sprintf("%q: \"...\"", in.Name))
		}
	}
	return "{" + strings.Join(parts, ", ") + "}"
}

func inputList(a *schema.Agent) string {
	if len(a.Inputs) == 0 {
		return "none"
	}
	var parts []string
	for _, in := range a.Inputs {
		part := "`" + in.Name + "`"
		if in.Optional || in.Default != nil || in.DefaultRef != "" {
			part += " (optional)"
		}
		parts = append(parts, part)
	}
	return strings.Join(parts, ", ")
}

func outputList(a *schema.Agent) string {
	if len(a.Outputs) == 0 {
		return "none"
	}
	var parts []string
	for _, out := range a.Outputs {
		parts = append(parts, "`"+out.Name+"`")
	}
	return strings.Join(parts, ", ")
}

// toolBinding is the one-line README summary of where a tool's calls go.
func toolBinding(t *schema.Tool) string {
	switch t.Source.Kind {
	case "mcp":
		if server, tool, err := parseMCPURI(t); err == nil {
			return fmt.Sprintf("MCP server `%s`, tool `%s`", server, tool)
		}
	case "http":
		return fmt.Sprintf("POST `%s`", t.Source.URI)
	case "runtime":
		return "user-implemented stub"
	}
	return "`" + t.Source.URI + "`"
}
