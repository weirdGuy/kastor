package langgraph

import (
	"fmt"
	"sort"
	"strings"

	"github.com/weirdGuy/agentform/internal/schema"
)

// provider is the langgraph-side mapping of an ADL model provider: the
// init_chat_model prefix, the langchain integration package that must be
// installed for it, and the environment variable holding its credentials
// ("" when the provider needs none).
type provider struct {
	Prefix string
	Dep    string
	EnvVar string
}

var providers = map[string]provider{
	"openai":    {Prefix: "openai", Dep: "langchain-openai", EnvVar: "OPENAI_API_KEY"},
	"anthropic": {Prefix: "anthropic", Dep: "langchain-anthropic", EnvVar: "ANTHROPIC_API_KEY"},
	"google":    {Prefix: "google_genai", Dep: "langchain-google-genai", EnvVar: "GOOGLE_API_KEY"},
	"ollama":    {Prefix: "ollama", Dep: "langchain-ollama", EnvVar: ""},
}

// supportedProviders lists the mapped providers for error messages, sorted.
func supportedProviders() string {
	names := make([]string, 0, len(providers))
	for name := range providers {
		names = append(names, name)
	}
	sort.Strings(names)
	return strings.Join(names, ", ")
}

// lookupProvider resolves a model's provider mapping or fails with the
// model's block address.
func lookupProvider(m *schema.Model) (provider, error) {
	p, ok := providers[m.Provider]
	if !ok {
		return provider{}, fmt.Errorf("%s: provider %q is not supported by the langgraph target (supported: %s)", m.Addr(), m.Provider, supportedProviders())
	}
	return p, nil
}

// genModels emits models.py: one factory function per model block, each
// returning an init_chat_model instance with the block's params as keyword
// arguments.
func genModels(models []*schema.Model) ([]byte, error) {
	var b strings.Builder
	b.WriteString("\"\"\"Model factories generated from the module's model blocks.\n\nGenerated by adl build. Do not edit.\n\"\"\"\n")
	if len(models) > 0 {
		b.WriteString("\nfrom langchain.chat_models import init_chat_model\n")
	}

	for _, m := range models {
		p, err := lookupProvider(m)
		if err != nil {
			return nil, err
		}

		fmt.Fprintf(&b, "\n\ndef %s():\n", pyIdent(m.Name))
		fmt.Fprintf(&b, "    \"\"\"%s — provider %q, id %q.\"\"\"\n", m.Addr(), m.Provider, m.ID)

		spec := pyString(p.Prefix + ":" + m.ID)
		if len(m.Params) == 0 {
			fmt.Fprintf(&b, "    return init_chat_model(%s)\n", spec)
			continue
		}

		keys := make([]string, 0, len(m.Params))
		for key := range m.Params {
			if pyIdent(key) != key {
				return nil, fmt.Errorf("%s: params key %q is not usable as a python keyword argument", m.Addr(), key)
			}
			keys = append(keys, key)
		}
		sort.Strings(keys)

		fmt.Fprintf(&b, "    return init_chat_model(\n        %s,\n", spec)
		for _, key := range keys {
			lit, err := pyLiteral(m.Params[key])
			if err != nil {
				return nil, fmt.Errorf("%s: params key %q: %w", m.Addr(), key, err)
			}
			fmt.Fprintf(&b, "        %s=%s,\n", key, lit)
		}
		b.WriteString("    )\n")
	}
	return []byte(b.String()), nil
}
