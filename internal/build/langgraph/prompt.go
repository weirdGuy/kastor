package langgraph

import (
	"fmt"
	"strings"

	"github.com/weirdGuy/agentform/internal/schema"
)

// genPromptsInit emits prompts/__init__.py: the shared {{var}} renderer.
// Its variable grammar mirrors SPEC.md §3.4 — {{ident}} with optional inner
// whitespace; anything else is body text and passes through untouched.
func genPromptsInit() []byte {
	return []byte(`"""Prompt templates and the {{var}} renderer.

Generated by adl build. Do not edit.
"""

import re

_VARIABLE = re.compile(r"\{\{\s*([A-Za-z_][A-Za-z0-9_]*)\s*\}\}")


def render(template, variables):
    """Substitute {{var}} references in an ADL prompt template.

    Missing and None values render as the empty string. Brace sequences
    that are not well-formed variables are body text per the ADL grammar
    and pass through untouched.
    """

    def substitute(match):
        value = variables.get(match.group(1))
        return "" if value is None else str(value)

    return _VARIABLE.sub(substitute, template)
`)
}

// genPrompt emits prompts/<name>.py holding the template body verbatim.
func genPrompt(p *schema.Prompt) []byte {
	var b strings.Builder
	fmt.Fprintf(&b, "\"\"\"%s — template body preserved verbatim from its .prompt source.\n\nGenerated by adl build. Do not edit.\n\"\"\"\n\n", p.Addr())
	b.WriteString("TEMPLATE = " + pyTriple(p.Body) + "\n")
	return []byte(b.String())
}
