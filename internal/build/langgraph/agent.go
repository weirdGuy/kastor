package langgraph

import (
	"fmt"
	"sort"
	"strings"

	"github.com/weirdGuy/agentform/internal/schema"
)

// genAgent emits agents/<name>.py: the agent's declared outputs as a
// pydantic model plus a run() that takes the declared inputs, renders the
// system prompt, and invokes a create_agent LangGraph agent.
func genAgent(a *schema.Agent) ([]byte, error) {
	params, err := agentParams(a)
	if err != nil {
		return nil, err
	}
	promptModule := ""
	if a.SystemPrompt != "" {
		promptModule = pyIdent(strings.TrimPrefix(a.SystemPrompt, "prompt."))
	}
	toolNames := make([]string, 0, len(a.Tools))
	for _, ref := range a.Tools {
		toolNames = append(toolNames, pyIdent(strings.TrimPrefix(ref, "tool.")))
	}
	sort.Strings(toolNames)

	var b strings.Builder
	writeAgentDoc(&b, a)
	writeAgentImports(&b, a, promptModule, toolNames)
	writeOutputModel(&b, a)

	ret := "str"
	if len(a.Outputs) > 0 {
		ret = "Output"
	}
	b.WriteString("\n\n" + signature("run", params, ret) + "\n")
	b.WriteString(docstring("    ", "Run "+a.Addr()+".", params))

	// The inputs double as prompt variables and, JSON-encoded, as the user
	// message — ADL has no separate user-message concept in v0.
	if len(params) == 0 {
		b.WriteString("    variables = {}\n")
	} else {
		sorted := append([]pyParam(nil), params...)
		sort.Slice(sorted, func(i, j int) bool { return sorted[i].source < sorted[j].source })
		b.WriteString("    variables = {\n")
		for _, p := range sorted {
			fmt.Fprintf(&b, "        %s: %s,\n", pyString(p.source), p.name)
		}
		b.WriteString("    }\n")
	}

	modelName := pyIdent(strings.TrimPrefix(a.Model, "model."))
	b.WriteString("    agent = create_agent(\n")
	fmt.Fprintf(&b, "        models.%s(),\n", modelName)
	fmt.Fprintf(&b, "        tools=[%s],\n", strings.Join(toolNames, ", "))
	if promptModule != "" {
		fmt.Fprintf(&b, "        system_prompt=prompts.render(%s.TEMPLATE, variables),\n", promptModule)
	}
	if len(a.Outputs) > 0 {
		b.WriteString("        response_format=Output,\n")
	}
	b.WriteString("    )\n")
	b.WriteString("    result = agent.invoke(\n")
	b.WriteString("        {\"messages\": [{\"role\": \"user\", \"content\": json.dumps(variables, sort_keys=True)}]}\n")
	b.WriteString("    )\n")
	if len(a.Outputs) > 0 {
		b.WriteString("    return result[\"structured_response\"]\n")
	} else {
		b.WriteString("    return result[\"messages\"][-1].content\n")
	}
	return []byte(b.String()), nil
}

// writeAgentDoc emits the module docstring. depends_on has no runtime
// meaning (SPEC.md §4: ordering only, never data flow), so it is recorded
// here and nowhere else.
func writeAgentDoc(b *strings.Builder, a *schema.Agent) {
	head := a.Addr()
	if a.Description != "" {
		head += " — " + a.Description
	}
	fmt.Fprintf(b, "\"\"\"%s\n\nGenerated by adl build. Do not edit.\n", head)
	if len(a.DependsOn) > 0 {
		fmt.Fprintf(b, "\nOrdering-only dependencies (depends_on): %s.\n", strings.Join(a.DependsOn, ", "))
	}
	b.WriteString("\"\"\"\n\n")
}

func writeAgentImports(b *strings.Builder, a *schema.Agent, promptModule string, toolNames []string) {
	b.WriteString("import json\n\nfrom langchain.agents import create_agent\n")
	if len(a.Outputs) > 0 {
		if outputsNeedField(a) {
			b.WriteString("from pydantic import BaseModel, Field\n")
		} else {
			b.WriteString("from pydantic import BaseModel\n")
		}
	}
	b.WriteString("\nimport models\n")
	if promptModule != "" {
		b.WriteString("import prompts\n")
		fmt.Fprintf(b, "from prompts import %s\n", promptModule)
	}
	for _, name := range toolNames {
		fmt.Fprintf(b, "from tools.%s import %s\n", name, name)
	}
}

// writeOutputModel emits the pydantic model backing create_agent's
// response_format — the codegen form of the agent-owned IO contract.
func writeOutputModel(b *strings.Builder, a *schema.Agent) {
	if len(a.Outputs) == 0 {
		return
	}
	b.WriteString("\n\nclass Output(BaseModel):\n")
	fmt.Fprintf(b, "    \"\"\"Outputs declared by %s.\"\"\"\n\n", a.Addr())
	for _, out := range a.Outputs {
		if out.Description != "" {
			fmt.Fprintf(b, "    %s: %s = Field(description=%s)\n", pyIdent(out.Name), pyType(out.Type), pyString(out.Description))
		} else {
			fmt.Fprintf(b, "    %s: %s\n", pyIdent(out.Name), pyType(out.Type))
		}
	}
}

func outputsNeedField(a *schema.Agent) bool {
	for _, out := range a.Outputs {
		if out.Description != "" {
			return true
		}
	}
	return false
}

// agentParams builds run()'s parameter list from the agent's inputs:
// required inputs first, then optional/defaulted ones, both in declaration
// order. An input whose default is a cross-agent reference becomes a plain
// optional parameter — v0 codegen does not wire cross-agent data flow.
func agentParams(a *schema.Agent) ([]pyParam, error) {
	var required, optional []pyParam
	for _, in := range a.Inputs {
		p := pyParam{
			name:   pyIdent(in.Name),
			source: in.Name,
			hint:   pyType(in.Type),
			doc:    inputDoc(in),
		}
		switch {
		case in.Default != nil:
			lit, err := pyLiteral(in.Default)
			if err != nil {
				return nil, fmt.Errorf("%s: input %q: %w", a.Addr(), in.Name, err)
			}
			p.def = lit
		case in.Optional || in.DefaultRef != "":
			p.hint += " | None"
			p.def = "None"
		}
		if p.def == "" {
			required = append(required, p)
		} else {
			optional = append(optional, p)
		}
	}
	return append(required, optional...), nil
}

func inputDoc(in *schema.AgentInput) string {
	switch {
	case in.Description != "" && in.DefaultRef != "":
		return fmt.Sprintf("%s (declared default: %s; pass the value explicitly)", in.Description, in.DefaultRef)
	case in.DefaultRef != "":
		return fmt.Sprintf("Declared default is %s; cross-agent data flow is not generated in v0, so pass the value explicitly.", in.DefaultRef)
	default:
		return in.Description
	}
}
