package langgraph

import (
	"fmt"
	"net/url"
	"strings"

	"github.com/weirdGuy/agentform/internal/schema"
)

// genTool emits tools/<name>.py for one tool block. The interface (params,
// returns) renders the same for every kind — a @tool function with typed
// parameters — while the body is the kind's binding.
func genTool(t *schema.Tool) ([]byte, error) {
	switch t.Source.Kind {
	case "mcp":
		return genMCPTool(t)
	case "http":
		return genHTTPTool(t)
	case "runtime":
		return genRuntimeTool(t)
	case "builtin":
		return nil, fmt.Errorf("%s: source kind \"builtin\" is platform-provided and has no langgraph codegen mapping; use a platform target for it", t.Addr())
	case "script":
		return nil, fmt.Errorf("%s: source kind \"script\" is not supported by the langgraph target yet", t.Addr())
	}
	return nil, fmt.Errorf("%s: unknown source kind %q", t.Addr(), t.Source.Kind)
}

// toolParams builds the generated function's parameter list: params without
// a default first, then defaulted ones, both in declaration order.
func toolParams(t *schema.Tool) ([]pyParam, error) {
	var required, optional []pyParam
	for _, param := range t.Params {
		p := pyParam{
			name:   pyIdent(param.Name),
			source: param.Name,
			hint:   pyType(param.Type),
			doc:    param.Description,
		}
		if param.Default != nil {
			lit, err := pyLiteral(param.Default)
			if err != nil {
				return nil, fmt.Errorf("%s: param %q: %w", t.Addr(), param.Name, err)
			}
			p.def = lit
		}
		if p.def == "" {
			required = append(required, p)
		} else {
			optional = append(optional, p)
		}
	}
	return append(required, optional...), nil
}

// toolSummary is the generated function's docstring summary — the tool
// description when present, since platforms surface it to the model.
func toolSummary(t *schema.Tool) string {
	if t.Description != "" {
		return t.Description
	}
	return "ADL tool " + t.Addr() + "."
}

// toolHeader emits the module docstring and imports shared by every kind.
func toolHeader(b *strings.Builder, t *schema.Tool, binding string, extraImports ...string) {
	head := t.Addr()
	if t.Description != "" {
		head += " — " + t.Description
	}
	fmt.Fprintf(b, "\"\"\"%s\n\nGenerated by adl build. Do not edit.\n\nBinding: %s\n\"\"\"\n\n", head, binding)
	for _, imp := range extraImports {
		b.WriteString(imp + "\n")
	}
	b.WriteString("\n\n@tool\n")
}

// parseMCPURI splits mcp://<server>/<tool> into its two parts.
func parseMCPURI(t *schema.Tool) (server, tool string, err error) {
	fail := func() (string, string, error) {
		return "", "", fmt.Errorf("%s: source uri %q: expected mcp://<server>/<tool>", t.Addr(), t.Source.URI)
	}
	u, parseErr := url.Parse(t.Source.URI)
	if parseErr != nil || u.Scheme != "mcp" || u.Host == "" {
		return fail()
	}
	tool = strings.TrimPrefix(u.Path, "/")
	if tool == "" || strings.Contains(tool, "/") {
		return fail()
	}
	return u.Host, tool, nil
}

// genMCPTool binds the tool to one tool on one MCP server via the emitted
// mcp_support bridge. Only the server and tool names come from the spec;
// how to reach the server is deployment config (see genMCPSupport).
func genMCPTool(t *schema.Tool) ([]byte, error) {
	server, name, err := parseMCPURI(t)
	if err != nil {
		return nil, err
	}
	params, err := toolParams(t)
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	toolHeader(&b, t,
		fmt.Sprintf("MCP server %q, tool %q (connection config in mcp_servers.json).", server, name),
		"from langchain.tools import tool",
		"",
		"from mcp_support import call_mcp_tool",
	)
	b.WriteString(signature(pyIdent(t.Name), params, pyType(t.Returns.Type)) + "\n")
	b.WriteString(docstring("    ", toolSummary(t), params))
	fmt.Fprintf(&b, "    result = call_mcp_tool(\n        %s,\n        %s,\n        %s,\n    )\n",
		pyString(server), pyString(name), argsDict(params, "        "))

	switch t.Returns.Type {
	case "number":
		b.WriteString("    return float(result)\n")
	case "bool":
		b.WriteString("    return bool(result)\n")
	default:
		b.WriteString("    return str(result)\n")
	}
	return []byte(b.String()), nil
}

// genHTTPTool binds the tool to a REST endpoint: parameters are POSTed as a
// JSON object to the source uri and the response body is coerced to the
// declared return type.
func genHTTPTool(t *schema.Tool) ([]byte, error) {
	u, err := url.Parse(t.Source.URI)
	if err != nil || (u.Scheme != "http" && u.Scheme != "https") || u.Host == "" {
		return nil, fmt.Errorf("%s: source uri %q is not a valid http(s) endpoint", t.Addr(), t.Source.URI)
	}
	params, err := toolParams(t)
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	toolHeader(&b, t,
		fmt.Sprintf("POSTs parameters as JSON to %q.", t.Source.URI),
		"import json",
		"from urllib.request import Request, urlopen",
		"",
		"from langchain.tools import tool",
	)
	b.WriteString(signature(pyIdent(t.Name), params, pyType(t.Returns.Type)) + "\n")
	b.WriteString(docstring("    ", toolSummary(t), params))
	fmt.Fprintf(&b, "    payload = json.dumps(%s, sort_keys=True).encode(\"utf-8\")\n", argsDict(params, "    "))
	fmt.Fprintf(&b, "    request = Request(\n        %s,\n        data=payload,\n        headers={\"Content-Type\": \"application/json\"},\n    )\n", pyString(t.Source.URI))
	b.WriteString("    with urlopen(request) as response:\n        body = response.read().decode(\"utf-8\")\n")

	switch t.Returns.Type {
	case "number":
		b.WriteString("    return float(body)\n")
	case "bool":
		b.WriteString("    return bool(json.loads(body))\n")
	default:
		b.WriteString("    return body\n")
	}
	return []byte(b.String()), nil
}

// genRuntimeTool emits the stub SPEC.md §3.3 promises for kind "runtime":
// the interface is generated, the body is the user's to supply.
func genRuntimeTool(t *schema.Tool) ([]byte, error) {
	params, err := toolParams(t)
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	toolHeader(&b, t,
		"runtime stub — the implementation is user code.",
		"from langchain.tools import tool",
	)
	b.WriteString(signature(pyIdent(t.Name), params, pyType(t.Returns.Type)) + "\n")
	b.WriteString(docstring("    ", toolSummary(t), params))
	b.WriteString("    # TODO: implement this runtime tool. adl build regenerates the file on\n")
	b.WriteString("    # every run, so keep the real logic in code you own and call it here.\n")
	fmt.Fprintf(&b, "    raise NotImplementedError(\n        %s\n    )\n",
		pyString(t.Addr()+` (kind = "runtime") has no implementation yet`))
	return []byte(b.String()), nil
}

// genMCPSupport emits mcp_support.py, the synchronous bridge generated MCP
// tools call through. The ADL spec pins a tool to mcp://<server>/<tool> but
// deliberately says nothing about transports or endpoints — those are
// deployment configuration, read at runtime from mcp_servers.json.
func genMCPSupport() []byte {
	return []byte(`"""Synchronous bridge from generated tools to MCP servers.

Generated by adl build. Do not edit.

The ADL spec pins a tool to an MCP server and tool name (mcp://<server>/<tool>)
but says nothing about how to reach the server: connection details are
deployment configuration. Put them in mcp_servers.json in the working
directory (or point ADL_MCP_CONFIG at a file). The file maps server names to
langchain-mcp-adapters connection dicts, e.g.:

    {
      "search-server": {
        "transport": "stdio",
        "command": "your-mcp-server",
        "args": []
      }
    }
"""

import asyncio
import json
import os

from langchain_mcp_adapters.client import MultiServerMCPClient

_CONFIG_ENV = "ADL_MCP_CONFIG"
_CONFIG_FILE = "mcp_servers.json"


def _connection(server):
    path = os.environ.get(_CONFIG_ENV, _CONFIG_FILE)
    try:
        with open(path, encoding="utf-8") as config:
            servers = json.load(config)
    except FileNotFoundError:
        raise RuntimeError(
            f"MCP server config {path!r} not found; create it or set {_CONFIG_ENV}"
        ) from None
    if server not in servers:
        raise KeyError(f"MCP server {server!r} is not configured in {path!r}")
    return servers[server]


def call_mcp_tool(server, tool, arguments):
    """Call one tool on one MCP server and return its result."""

    async def _call():
        client = MultiServerMCPClient({server: _connection(server)})
        tools = await client.get_tools(server_name=server)
        for candidate in tools:
            if candidate.name == tool:
                return await candidate.ainvoke(arguments)
        raise KeyError(f"MCP server {server!r} does not expose tool {tool!r}")

    return asyncio.run(_call())
`)
}
