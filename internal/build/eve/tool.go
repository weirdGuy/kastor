package eve

import (
	"fmt"
	"net/url"
	"strings"

	"github.com/weirdGuy/kastor/internal/schema"
)

// genTool emits tools/<name>.ts for one tool block, or reports that the
// tool's kind has no eve file binding:
//
//	http    → defineTool whose execute POSTs the params as JSON to the uri
//	runtime → defineTool stub throwing until user code supplies the body
//	mcp     → no tools/ file: the server connection surfaces the tool
//	          (see genConnection); genTool must not be called for it
//	builtin → codegen error, permanently (platform-provided, no local binding)
//	script  → codegen error, for now (glue-code execution deferred, issue #36)
func genTool(t *schema.Tool) ([]byte, error) {
	switch t.Source.Kind {
	case "http":
		return genHTTPTool(t)
	case "runtime":
		return genRuntimeTool(t)
	case "builtin":
		return nil, fmt.Errorf("%s: source kind \"builtin\" is platform-provided and has no eve codegen mapping; use a platform target for it", t.Addr())
	case "script":
		return nil, fmt.Errorf("%s: source kind \"script\" is not supported by the eve target yet", t.Addr())
	}
	return nil, fmt.Errorf("%s: unknown source kind %q", t.Addr(), t.Source.Kind)
}

// toolSummary is the generated tool's description — the spec's description
// when present, since eve surfaces it to the model.
func toolSummary(t *schema.Tool) string {
	if t.Description != "" {
		return t.Description
	}
	return "Kastor tool " + t.Addr() + "."
}

// toolHeader emits the comment header and imports shared by both file-backed
// kinds.
func toolHeader(b *strings.Builder, t *schema.Tool, binding string) {
	head := t.Addr()
	if t.Description != "" {
		head += " — " + t.Description
	}
	fmt.Fprintf(b, "// %s\n//\n// Generated by kastor build. Do not edit.\n//\n// Binding: %s\n", head, binding)
	b.WriteString("\nimport { defineTool } from \"eve/tools\";\nimport { z } from \"zod\";\n")
}

// zodSchema renders the tool's params as a z.object literal at indent. A
// param with a default is optional at call time (SPEC.md §3.3), which is
// exactly Zod's .default semantics.
func zodSchema(t *schema.Tool, indent string) (string, error) {
	if len(t.Params) == 0 {
		return "z.object({})", nil
	}
	var b strings.Builder
	b.WriteString("z.object({\n")
	for _, p := range t.Params {
		chain := "z." + zodType(p.Type) + "()"
		if p.Description != "" {
			chain += ".describe(" + tsString(p.Description) + ")"
		}
		if p.Default != nil {
			lit, err := tsLiteral(p.Default)
			if err != nil {
				return "", fmt.Errorf("%s: param %q: %w", t.Addr(), p.Name, err)
			}
			chain += ".default(" + lit + ")"
		}
		b.WriteString(indent + "  " + tsKey(p.Name) + ": " + chain + ",\n")
	}
	b.WriteString(indent + "})")
	return b.String(), nil
}

// zodType maps an Kastor type keyword to its Zod constructor name.
func zodType(t string) string {
	switch t {
	case "string":
		return "string"
	case "number":
		return "number"
	case "bool":
		return "boolean"
	}
	return "unknown"
}

// genHTTPTool binds the tool to a REST endpoint: parameters are POSTed as a
// JSON object to the source uri and the response body is coerced to the
// declared return type — the same binding as the langgraph target.
func genHTTPTool(t *schema.Tool) ([]byte, error) {
	u, err := url.Parse(t.Source.URI)
	if err != nil || (u.Scheme != "http" && u.Scheme != "https") || u.Host == "" {
		return nil, fmt.Errorf("%s: source uri %q is not a valid http(s) endpoint", t.Addr(), t.Source.URI)
	}
	schema, err := zodSchema(t, "  ")
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	toolHeader(&b, t, fmt.Sprintf("POSTs parameters as JSON to %q.", t.Source.URI))
	b.WriteString("\nexport default defineTool({\n")
	fmt.Fprintf(&b, "  description: %s,\n", tsString(toolSummary(t)))
	fmt.Fprintf(&b, "  inputSchema: %s,\n", schema)
	b.WriteString("  async execute(input) {\n")
	fmt.Fprintf(&b, "    const response = await fetch(%s, {\n", tsString(t.Source.URI))
	b.WriteString("      method: \"POST\",\n")
	b.WriteString("      headers: { \"Content-Type\": \"application/json\" },\n")
	b.WriteString("      body: JSON.stringify(input),\n")
	b.WriteString("    });\n")
	b.WriteString("    if (!response.ok) {\n")
	fmt.Fprintf(&b, "      throw new Error(%s + response.status);\n",
		tsString(t.Addr()+": POST "+t.Source.URI+" failed with status "))
	b.WriteString("    }\n")
	b.WriteString("    const body = await response.text();\n")
	switch t.Returns.Type {
	case "number":
		b.WriteString("    return Number(body);\n")
	case "bool":
		b.WriteString("    return Boolean(JSON.parse(body));\n")
	default:
		b.WriteString("    return body;\n")
	}
	b.WriteString("  },\n});\n")
	return []byte(b.String()), nil
}

// genRuntimeTool emits the stub SPEC.md §3.3 promises for kind "runtime":
// the typed interface is generated, the body is the user's to supply.
func genRuntimeTool(t *schema.Tool) ([]byte, error) {
	schema, err := zodSchema(t, "  ")
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	toolHeader(&b, t, "runtime stub — the implementation is user code.")
	b.WriteString("\nexport default defineTool({\n")
	fmt.Fprintf(&b, "  description: %s,\n", tsString(toolSummary(t)))
	fmt.Fprintf(&b, "  inputSchema: %s,\n", schema)
	b.WriteString("  async execute() {\n")
	b.WriteString("    // TODO: implement this runtime tool. kastor build regenerates the file on\n")
	b.WriteString("    // every run, so keep the real logic in code you own and call it here.\n")
	fmt.Fprintf(&b, "    throw new Error(%s);\n", tsString(t.Addr()+` (kind = "runtime") has no implementation yet`))
	b.WriteString("  },\n});\n")
	return []byte(b.String()), nil
}
