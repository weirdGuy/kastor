package eve

import (
	"fmt"
	"strings"

	"github.com/weirdGuy/kastor/internal/schema"
)

// eveVersion is the eve release the generated project pins. Exact on
// purpose: eve is in public beta and its APIs may change before GA, so a
// range would let a rebuild-free project drift onto a breaking release.
const eveVersion = "0.11.4"

// aiVersion satisfies eve's only required peer dependency, which eve pins
// exactly (npm refuses to install otherwise). It must move in lockstep with
// eveVersion: check `npm view eve@<version> peerDependencies` when bumping.
const aiVersion = "7.0.0-beta.178"

// zodVersion is the floor the pinned ai release accepts (^3.25.76 || ^4.1.8).
const zodVersion = "^3.25.76"

// genPackageJSON emits the project manifest. The eve dependency is pinned
// exactly (see eveVersion) and ai/zod match its peer requirements.
func genPackageJSON(root *schema.Agent) []byte {
	var b strings.Builder
	b.WriteString("{\n")
	fmt.Fprintf(&b, "  \"name\": %s,\n", tsString(strings.ToLower(root.Name)))
	b.WriteString("  \"private\": true,\n")
	b.WriteString("  \"type\": \"module\",\n")
	b.WriteString("  \"engines\": { \"node\": \">=24\" },\n")
	b.WriteString("  \"scripts\": {\n")
	b.WriteString("    \"build\": \"eve build\",\n")
	b.WriteString("    \"dev\": \"eve dev\",\n")
	b.WriteString("    \"start\": \"eve start\"\n")
	b.WriteString("  },\n")
	b.WriteString("  \"dependencies\": {\n")
	fmt.Fprintf(&b, "    \"ai\": %s,\n", tsString(aiVersion))
	fmt.Fprintf(&b, "    \"eve\": %s,\n", tsString(eveVersion))
	fmt.Fprintf(&b, "    \"zod\": %s\n", tsString(zodVersion))
	b.WriteString("  }\n")
	b.WriteString("}\n")
	return []byte(b.String())
}

// genTsconfig emits the TypeScript config eve projects expect: modern
// Node-flavored ESM, strict, with eve build owning emission.
func genTsconfig() []byte {
	return []byte(`{
  "compilerOptions": {
    "target": "ES2022",
    "module": "NodeNext",
    "moduleResolution": "NodeNext",
    "strict": true,
    "skipLibCheck": true,
    "noEmit": true
  }
}
`)
}

// genReadme emits the project README: provenance, setup, credentials, the
// agent tree, and every deliberate mapping degradation, so nothing the
// generator decided is discoverable only by diffing spec against output.
func (pb *projectBuilder) genReadme() []byte {
	var b strings.Builder
	fmt.Fprintf(&b, "# %s — eve project\n\n", pb.root.Addr())
	b.WriteString("Generated by `kastor build`. Do not edit files in this directory: the next\n")
	b.WriteString("build rewrites them and removes visible files it did not generate — that\n")
	b.WriteString("includes `node_modules/` and `package-lock.json`, so re-run `npm install`\n")
	b.WriteString("after rebuilding. Hidden entries (`.eve/`, `.env.local`, …) are left alone.\n")

	b.WriteString("\n## Setup\n\n```sh\nnpm install\nnpx eve build\nnpx eve dev\n```\n\n")
	b.WriteString("An eve agent is an ordinary Vercel project: `vercel deploy` ships it unchanged.\n")

	pb.writeModels(&b)
	pb.writeAgents(&b)
	pb.writeServers(&b)
	pb.writeRuntime(&b)
	pb.writeSkills(&b)
	pb.writeUnbound(&b)
	return []byte(b.String())
}

func (pb *projectBuilder) writeModels(b *strings.Builder) {
	b.WriteString("\n## Models\n\nModels route through the Vercel AI Gateway: set `AI_GATEWAY_API_KEY`, or rely\non project OIDC when the agent is deployed on Vercel.\n\n")
	for _, name := range sortedKeys(pb.models) {
		m := pb.models[name]
		fmt.Fprintf(b, "- `%s` — provider `%s`, routed as `%s/%s`\n", m.Addr(), m.Provider, gatewayPrefixes[m.Provider], m.ID)
	}
}

func (pb *projectBuilder) writeAgents(b *strings.Builder) {
	b.WriteString("\n## Agents\n\n")
	for _, ta := range pb.tree {
		dir := strings.TrimPrefix(strings.TrimSuffix(ta.dir, "/"), pb.root.Name+"/")
		fmt.Fprintf(b, "- `%s` (`%s`)", ta.agent.Addr(), dir)
		switch {
		case ta.parent == "":
			b.WriteString(" — root")
		case ta.orderingOnly:
			fmt.Fprintf(b, " — ordering-only dependency (`depends_on`) of `agent.%s`; kastor\n  orders it before its dependent but implies no delegation — eve makes it an\n  invokable subagent, which is a strictly wider capability", ta.parent)
		default:
			fmt.Fprintf(b, " — referenced by `agent.%s`", ta.parent)
		}
		b.WriteString("\n")
	}
	b.WriteString("\nEach agent's typed IO contract (its input/output blocks) has no eve\nequivalent. It degrades to convention: generated Inputs/Outputs sections in\nthat agent's instructions.md, which the model is instructed to follow but\nnothing enforces. Cross-agent input defaults are likewise not wired — the\ninstructions tell the model to delegate to the subagent or use the value\nfrom the message.\n")
}

func (pb *projectBuilder) writeServers(b *strings.Builder) {
	if len(pb.servers) == 0 {
		return
	}
	b.WriteString("\n## MCP servers\n\nThe spec pins tool identity only (`mcp://<server>/<tool>`); each connection\nfile allow-lists exactly the pinned tools and reads its endpoint URL — a\nStreamable HTTP or SSE endpoint — from the environment. A stdio-only local\nserver needs an HTTP bridge in front of it.\n\n")
	for _, name := range sortedKeys(pb.servers) {
		s := pb.servers[name]
		fmt.Fprintf(b, "- server `%s`: set `%s` (tools: %s; from %s)\n", s.Name, envName(s.Name), backtickList(s.Allow), boundBy(s))
	}
	b.WriteString("\nHosted endpoints often embed credentials in the URL, so keep these variables\nin deployment config (e.g. `.env.local`), never in version control.\n")
}

func (pb *projectBuilder) writeRuntime(b *strings.Builder) {
	if len(pb.runtime) == 0 {
		return
	}
	b.WriteString("\n## Runtime tools\n\nTools with `kind = \"runtime\"` are generated as stubs that throw until you\nsupply the body. `kastor build` regenerates them on every run, so keep the\nreal implementation in code you own and call it from the stub:\n\n")
	for _, t := range sortedCopy(pb.runtime, (*schema.Tool).Addr) {
		fmt.Fprintf(b, "- `%s`\n", t.Addr())
	}
}

func (pb *projectBuilder) writeSkills(b *strings.Builder) {
	if len(pb.idx.unusedPrompts) == 0 {
		return
	}
	b.WriteString("\n## Skills\n\nPrompts that are no agent's system_prompt are emitted as on-demand skills\n(`agent/skills/`), loadable by the model when relevant:\n\n")
	for _, p := range pb.idx.unusedPrompts {
		fmt.Fprintf(b, "- `%s` → `agent/skills/%s.md`\n", p.Addr(), p.Name)
	}
}

func (pb *projectBuilder) writeUnbound(b *strings.Builder) {
	if len(pb.idx.unboundTools) == 0 && len(pb.idx.unboundModels) == 0 {
		return
	}
	b.WriteString("\n## Not emitted\n\nDeclared in the module but referenced by no agent, so they have no place in\nan eve project — a file in `agent/tools/` is auto-wired into the agent, and\na model materializes only inside some agent's agent.ts:\n\n")
	for _, t := range pb.idx.unboundTools {
		fmt.Fprintf(b, "- `%s` (kind `%s`)\n", t.Addr(), t.Source.Kind)
	}
	for _, m := range pb.idx.unboundModels {
		fmt.Fprintf(b, "- `%s` (provider `%s`)\n", m.Addr(), m.Provider)
	}
}

func backtickList(names []string) string {
	parts := make([]string, len(names))
	for i, n := range names {
		parts[i] = "`" + n + "`"
	}
	return strings.Join(parts, ", ")
}
