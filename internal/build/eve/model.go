package eve

import (
	"fmt"
	"strings"

	"github.com/weirdGuy/kastor/internal/schema"
)

// gatewayPrefixes maps an Kastor model provider to its Vercel AI Gateway
// namespace: agent.ts pins the model as "<prefix>/<id>" and the gateway
// routes the call (credentials: AI_GATEWAY_API_KEY, or project OIDC when
// deployed on Vercel). Providers the gateway cannot route — ollama is a
// local runtime — have no eve mapping and are codegen errors.
var gatewayPrefixes = map[string]string{
	"openai":    "openai",
	"anthropic": "anthropic",
	"google":    "google",
}

// supportedProviders lists the mapped providers for error messages, sorted.
func supportedProviders() string {
	return strings.Join(sortedKeys(gatewayPrefixes), ", ")
}

// lookupPrefix resolves a model's gateway namespace or fails with the
// model's block address.
func lookupPrefix(m *schema.Model) (string, error) {
	p, ok := gatewayPrefixes[m.Provider]
	if !ok {
		return "", fmt.Errorf("%s: provider %q is not supported by the eve target (supported: %s)", m.Addr(), m.Provider, supportedProviders())
	}
	return p, nil
}

// genAgentTS emits an agent directory's agent.ts: defineAgent with the
// agent's name, its model as a gateway model string, and the model block's
// params passed through verbatim as modelOptions (provider-specific keys,
// exactly as in the spec — SPEC.md §3.1 says targets do not validate them).
func genAgentTS(a *schema.Agent, m *schema.Model) ([]byte, error) {
	prefix, err := lookupPrefix(m)
	if err != nil {
		return nil, err
	}

	var b strings.Builder
	head := a.Addr()
	if a.Description != "" {
		head += " — " + a.Description
	}
	fmt.Fprintf(&b, "// %s\n//\n// Generated by kastor build. Do not edit.\n", head)
	if len(a.DependsOn) > 0 {
		fmt.Fprintf(&b, "//\n// Ordering-only dependencies (depends_on): %s.\n", strings.Join(a.DependsOn, ", "))
	}
	b.WriteString("\nimport { defineAgent } from \"eve\";\n")
	b.WriteString("\nexport default defineAgent({\n")
	fmt.Fprintf(&b, "  name: %s,\n", tsString(a.Name))
	fmt.Fprintf(&b, "  model: %s,\n", tsString(prefix+"/"+m.ID))

	if len(m.Params) > 0 {
		b.WriteString("  modelOptions: {\n")
		for _, key := range sortedKeys(m.Params) {
			lit, err := tsLiteral(m.Params[key])
			if err != nil {
				return nil, fmt.Errorf("%s: params key %q: %w", m.Addr(), key, err)
			}
			fmt.Fprintf(&b, "    %s: %s,\n", tsKey(key), lit)
		}
		b.WriteString("  },\n")
	}
	b.WriteString("});\n")
	return []byte(b.String()), nil
}
